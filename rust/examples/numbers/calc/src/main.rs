fn main() {
    let x = 23;
    let y = 19;
    println!("{}", x);
    println!("{}", y);

    let add = x + y;
    println!("add: {}", add);

    let multiple = x * y;
    println!("multiple: {}", multiple);

    let neg = -x;
    println!("neg: {}", neg);

    let diff = y - x;
    println!("diff: {}", diff);

    let div = x / y;
    println!("div: {}", div);

    let modulus = x % y;
    println!("mod: {}", modulus);
}
