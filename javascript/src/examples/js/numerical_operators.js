"use strict";

console.log(19 + 23);   // 42
console.log(23 - 19);   // 4
console.log(23 * 19);   // 437
console.log(23 / 19);   // 1.2105263157894737
console.log(123 % 19);  // 9   (modulus)

var x = 19;
var y = 23;
console.log(x + y); // 42

x++;
console.log(x);   // 20

y--;
console.log(y);  // 22

x += 7;    // x = x + 7;
console.log(x);   // 27

x /= 3;   // x  = x / 3;
console.log(x);   // 9
