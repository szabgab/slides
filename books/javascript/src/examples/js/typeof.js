console.log(typeof 42);            // number

var answer = 52;
console.log(typeof answer);        // number

var name;
console.log(typeof name);          // string

console.log(typeof 1/0);           // Nan
console.log(typeof undefined);     // undefined
console.log(typeof null);          // object
